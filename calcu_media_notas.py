# Calcular a média de três notas fornecidas na entrada do usuário. Utilize operadores aritméticos para realizar o cálculo da média.

# Solicitando as três notas como entrada do usuário
nota1 = float(input("Digite a primeira nota: "))
nota2 = float(input("Digite a segunda nota: "))
nota3 = float(input("Digite a terceira nota: "))

# Calculando a média das três notas
media = (nota1 + nota2 + nota3) / 3

# Exibindo o resultado da média
print("A média das notas é:", media)   
